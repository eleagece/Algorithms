    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        for(int i=0; i<jTable1.getRowCount();i++){
            Boolean eliminar = (Boolean)jTable1.getValueAt(i,0);
            
            if(eliminar){
                BBDD bd = new BBDD();
                bd.abrirConexion();
                String DNI = (String) jTable1.getValueAt(i,5);   
                Integer dni  = Integer.valueOf(DNI);                
                bd.borrarTusuarios(dni);
                bd.cerrarConexion();
            } else{
                BBDD bd = new BBDD();
                bd.abrirConexion();
                String DNI = (String) jTable1.getValueAt(i,5);   
                Integer dni  = Integer.valueOf(DNI); 

                bd.modificarTusuarios("Nombre",(String)jTable1.getValueAt(i,1),dni);
                bd.modificarTusuarios("Apellido1",(String)jTable1.getValueAt(i,2),dni);    
                bd.modificarTusuarios("Apellido2",(String)jTable1.getValueAt(i,3),dni);
                bd.modificarTusuarios("Email",(String)jTable1.getValueAt(i,4),dni);
                //bd.modificar("DNI",(Integer)jTable1.getValueAt(i,5),dni);
                bd.modificarTusuarios("Usuario",(String)jTable1.getValueAt(i,6),dni);
                bd.modificarTusuarios("Password",(String)jTable1.getValueAt(i,7),dni);
                bd.modificarTusuarios("Perfil",(String)jTable1.getValueAt(i,8),dni);
                
                Object pt = (Object)jTable1.getValueAt(i,9);
                String p = String.valueOf(pt);                
                bd.modificarTusuarios("Puntos",p,dni);

                bd.cerrarConexion();
            }
            
        }        
        
        
        VentAdmin vAdmin=new VentAdmin(vRaiz);
            vAdmin.setVisible(true);
            vAdmin.setSize(2000,2000);
            vRaiz.remove(this);
            vRaiz.add(vAdmin);
            vRaiz.pack();
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
