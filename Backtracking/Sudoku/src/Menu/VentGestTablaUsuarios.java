    public void pintar(){
        BBDD bd = new BBDD();
        bd.abrirConexion();


            
            ArrayList usuarios = new ArrayList();
            usuarios = bd.obtenerTusuarioss();

            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            for(int i=0;i<usuarios.size();i++){
                ArrayList usuario = new ArrayList();
                usuario = (ArrayList) usuarios.get(i);                
                
                model.addRow(new Object[]{});
                jTable1.setValueAt(false,i,0);
                jTable1.setValueAt(usuario.get(0),i,1);
                jTable1.setValueAt(usuario.get(1),i,2);
                jTable1.setValueAt(usuario.get(2),i,3);
                jTable1.setValueAt(usuario.get(3),i,4);
                jTable1.setValueAt(usuario.get(4),i,5);
                jTable1.setValueAt(usuario.get(5),i,6);
                jTable1.setValueAt(usuario.get(6),i,7);
                jTable1.setValueAt(usuario.get(7),i,8);       
                jTable1.setValueAt(usuario.get(8),i,9);      
            }

            bd.cerrarConexion();
    }    
    
