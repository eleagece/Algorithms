    public void pintar(){
        BBDD bd = new BBDD();
        bd.abrirConexion();


            
            ArrayList competiciones = new ArrayList();
            competiciones = bd.obtenerTcompeticioness();

            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            for(int i=0;i<competiciones.size();i++){
                ArrayList competicion = new ArrayList();
                competicion = (ArrayList) competiciones.get(i);                
                
                model.addRow(new Object[]{});
                jTable1.setValueAt(false,i,0);
                jTable1.setValueAt(competicion.get(0),i,1);
                jTable1.setValueAt(competicion.get(1),i,2);
                jTable1.setValueAt(competicion.get(2),i,3);
                jTable1.setValueAt(competicion.get(3),i,4);
                jTable1.setValueAt(competicion.get(4),i,5);
                jTable1.setValueAt(competicion.get(5),i,6);
            }

            bd.cerrarConexion();
    }         
    
    
