        for(int i=0; i<jTable1.getRowCount();i++){
            Boolean eliminar = (Boolean)jTable1.getValueAt(i,0);
            
            if(eliminar){
                BBDD bd = new BBDD();
                bd.abrirConexion();
                String ID = (String) jTable1.getValueAt(i,1);   
                Integer id  = Integer.valueOf(ID);                
                bd.borrarTcompeticion(id);
                bd.cerrarConexion();
            } else{
                BBDD bd = new BBDD();
                bd.abrirConexion();
                String ID = (String) jTable1.getValueAt(i,1);   
                Integer id  = Integer.valueOf(ID);
                bd.modificarTcompeticion("Sudoku1",(String)jTable1.getValueAt(i,2),id);
                bd.modificarTcompeticion("Sudoku2",(String)jTable1.getValueAt(i,3),id);
                bd.modificarTcompeticion("Sudoku3",(String)jTable1.getValueAt(i,4),id);
                bd.modificarTcompeticion("Sudoku4",(String)jTable1.getValueAt(i,5),id);
                
                String Tiempo = (String) jTable1.getValueAt(i,6);   
                Integer temp  = Integer.valueOf(Tiempo);                     
                bd.modificarTcompeticion("Tiempo",(String) jTable1.getValueAt(i,6),id);    
                bd.cerrarConexion();
            }
            
        }
        VentAdmin vAdmin=new VentAdmin(vRaiz);
            vAdmin.setVisible(true);
            vAdmin.setSize(2000,2000);
