    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for(int i=0; i<jTable1.getRowCount();i++){
            Boolean eliminar = (Boolean)jTable1.getValueAt(i,0);
            
            if(eliminar){
                BBDD bd = new BBDD();
                bd.abrirConexion();
                String Concepto = (String) jTable1.getValueAt(i,1);   
                //Integer id  = Integer.valueOf(ID);                
                bd.borrarTconfiguracion(Concepto);
                bd.cerrarConexion();
            } else{
                BBDD bd = new BBDD();
                bd.abrirConexion();
                String Concepto = (String) jTable1.getValueAt(i,1);     
                
                Object vl = (Object)jTable1.getValueAt(i,2);
                String v = String.valueOf(vl);                  
                bd.modificarTconfiguracion("Valor",v,Concepto);   
                bd.cerrarConexion();
            }
            
        }
        VentAdmin vAdmin=new VentAdmin(vRaiz);
            vAdmin.setVisible(true);
            vAdmin.setSize(2000,2000);
            vRaiz.remove(this);
            vRaiz.add(vAdmin);
            vRaiz.pack(); 
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
