    public void pintar(){
        BBDD bd = new BBDD();
        bd.abrirConexion();


            
            ArrayList configuraciones = new ArrayList();
            configuraciones = bd.obtenerTconfiguraciones();

            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            for(int i=0;i<configuraciones.size();i++){
                ArrayList configuracion = new ArrayList();
                configuracion = (ArrayList) configuraciones.get(i);                
                
                model.addRow(new Object[]{});
                jTable1.setValueAt(false,i,0);
                jTable1.setValueAt(configuracion.get(0),i,1);
                jTable1.setValueAt(configuracion.get(1),i,2);       
            }

            bd.cerrarConexion();
    }      
    
    
