    public void pintar(){
        BBDD bd = new BBDD();
        bd.abrirConexion();


            
            ArrayList estadocompeticiones = new ArrayList();
            estadocompeticiones = bd.obtenerEstadoCompeticiones();

            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            for(int i=0;i<estadocompeticiones.size();i++){
                ArrayList estadocompeticion = new ArrayList();
                estadocompeticion = (ArrayList) estadocompeticiones.get(i);                
                
                model.addRow(new Object[]{});
                jTable1.setValueAt(false,i,0);
                jTable1.setValueAt(estadocompeticion.get(0),i,1);
                jTable1.setValueAt(estadocompeticion.get(1),i,2);
                jTable1.setValueAt(estadocompeticion.get(2),i,3);
                jTable1.setValueAt(estadocompeticion.get(3),i,4);
                jTable1.setValueAt(estadocompeticion.get(4),i,5);
                jTable1.setValueAt(estadocompeticion.get(5),i,6);
                jTable1.setValueAt(estadocompeticion.get(6),i,7);
            }

            bd.cerrarConexion();
    }     
    
