    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       for(int i=0; i<jTable1.getRowCount();i++){
            Boolean eliminar = (Boolean)jTable1.getValueAt(i,0);
            
            if(eliminar){
                BBDD bd = new BBDD();
                bd.abrirConexion();
                Integer id = (Integer) jTable1.getValueAt(i,1);
                Integer dni = (Integer) jTable1.getValueAt(i,2);                              
                bd.borrarTestadocompeticion(id,dni);
                bd.cerrarConexion();
            } else{
                BBDD bd = new BBDD();
                bd.abrirConexion();
                Integer id = (Integer) jTable1.getValueAt(i,1);   
                Integer dni = (Integer) jTable1.getValueAt(i,2); 
                //Integer id  = Integer.valueOf(ID);
                bd.modificarTestadocompeticion("Puntos",(Integer)jTable1.getValueAt(i,3),id,dni);
                bd.modificarTestadocompeticion("Sudoku1",(Boolean)jTable1.getValueAt(i,4),id,dni);
                bd.modificarTestadocompeticion("Sudoku2",(Boolean)jTable1.getValueAt(i,5),id,dni);
                bd.modificarTestadocompeticion("Sudoku3",(Boolean)jTable1.getValueAt(i,6),id,dni);
                bd.modificarTestadocompeticion("Sudoku4",(Boolean)jTable1.getValueAt(i,7),id,dni);   
                bd.cerrarConexion();
            }
            
        }
        VentAdmin vAdmin=new VentAdmin(vRaiz);
            vAdmin.setVisible(true);
            vAdmin.setSize(2000,2000);
            vRaiz.remove(this);
            vRaiz.add(vAdmin);
            vRaiz.pack(); 
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
