    public void pintar(){
        BBDD bd = new BBDD();
        bd.abrirConexion();


            
            ArrayList sudokus = new ArrayList();
            sudokus = bd.obtenerTsudokuss();

            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            for(int i=0;i<sudokus.size();i++){
                ArrayList sudoku = new ArrayList();
                sudoku = (ArrayList) sudokus.get(i);                
                
                model.addRow(new Object[]{});
                jTable1.setValueAt(false,i,0);
                jTable1.setValueAt(sudoku.get(0),i,1);
                jTable1.setValueAt(sudoku.get(1),i,2);
                jTable1.setValueAt(sudoku.get(2),i,3);          
            }

            bd.cerrarConexion();
    }        
    
