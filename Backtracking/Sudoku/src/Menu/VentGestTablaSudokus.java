    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        for(int i=0; i<jTable1.getRowCount();i++){
            Boolean eliminar = (Boolean)jTable1.getValueAt(i,0);
            
            if(eliminar){
                BBDD bd = new BBDD();
                bd.abrirConexion();
                String ID = (String) jTable1.getValueAt(i,1);   
                Integer id  = Integer.valueOf(ID);                
                bd.borrarTsudokus(id);
                bd.cerrarConexion();
            } else{
                BBDD bd = new BBDD();
                bd.abrirConexion();
                String ID = (String) jTable1.getValueAt(i,1);   
                Integer id  = Integer.valueOf(ID);
                bd.modificarTsudokus("Nombre",(String)jTable1.getValueAt(i,2),id);
                
                String Dificultad = (String) jTable1.getValueAt(i,3);   
                Integer dif  = Integer.valueOf(Dificultad);                     
                bd.modificarTsudokus("Dificultad",dif,id);    
                bd.cerrarConexion();
            }
            
        }
        VentAdmin vAdmin=new VentAdmin(vRaiz);
            vAdmin.setVisible(true);
            vAdmin.setSize(2000,2000);
            vRaiz.remove(this);
            vRaiz.add(vAdmin);
            vRaiz.pack();        
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
